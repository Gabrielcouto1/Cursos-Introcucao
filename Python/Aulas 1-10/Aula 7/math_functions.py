import math 

pi = 3.14
x = 1 
y = 2
z = 3

print(round(pi))        #rounds to the closest integer
print(math.ceil(pi))    #rounds to the the ceil integer
print(math.floor(pi))   #rounds to the floor integer
print(abs(pi))          #turns negative values to positive
print(pow(pi,2))        #returns the first argument to the potency of the second argument
print(math.sqrt(pi))    #returns the square root of the number
print(max(x,y,z))       #returns the largest value of the arguments inserted
print(min(x,y,z))       #returns the smallest value of the arguments inserted